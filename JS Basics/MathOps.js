/******************** Mathematical Operations ***********************/

//random number between 0 and 1
console.log(Math.random())          //0.8190783597498867

//absolute value
console.log(Math.abs(10-3))         //7
console.log(Math.abs(7-10))         //3

//max value
console.log(Math.max(1, 2, 3, 4))   //4

//min value
console.log(Math.min(1, 2, 3, 4))   //1

//sign of a number
console.log(Math.sign(10))          //1
console.log(Math.sign(0))           //0
console.log(Math.sign(-10))         //-1

//truncate the integer
console.log(Math.trunc(7.38905))    //7

//hypotenuse of a triangle
console.log(Math.hypot(3, 4))       //5

//power
console.log(Math.pow(5, 4))         //625

//square root of 2
console.log(Math.SQRT2)             //1.4142135623730951

//square root of (1/2)
console.log(Math.SQRT1_2)           //0.7071067811865476

//square root
console.log(Math.sqrt(16))          //4

//cube root
console.log(Math.cbrt(216))         //6

//round off
console.log(Math.round(4.49))       //4
console.log(Math.round(4.50))       //5

//ceiling
console.log(Math.ceil(4.49))        //5

//floor
console.log(Math.floor(4.49))       //4

//value of e
console.log(Math.E)                 //2.718281828459045

//exponential (e^2)
console.log(Math.exp(2))            //7.38905609893065

//exponential (e^2-1)
console.log(Math.expm1(2))          //6.38905609893065

//logarithm of 10
console.log(Math.LN10)              //2.302585092994046

//logarithm of 2
console.log(Math.LN2)               //0.6931471805599453

//10-base logarithm of e
console.log(Math.LOG10E)            //0.4342944819032518

//2-base logarithm of e
console.log(Math.LOG2E)             //1.4426950408889634

//logarithm base e
console.log(Math.log(7.38905609))   //1.999999998791368

//logarithm base 10
console.log(Math.log10(100))        //2

//logarithm base 2
console.log(Math.log2(16))          //4

//trigonometric ratio
console.log(Math.sin(Math.PI/4))    //0.7071067811865475
console.log(Math.cos(Math.PI/4))    //0.7071067811865476
console.log(Math.tan(Math.PI/4))    //0.9999999999999999

